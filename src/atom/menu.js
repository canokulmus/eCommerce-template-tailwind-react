//activate menu 
export const activateMenu = (pathname, menu, setMenu) => {
    let newMenu = menu.map(item => {
        if (item.link === pathname) {
            item.active = true
        } else {
            item.active = false
        }
        return item
    })
    setMenu(newMenu)
}
